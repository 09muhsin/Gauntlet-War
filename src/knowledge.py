# Machine-readable subset of the ban for a deterministic post-sweep — high-signal phrases that are
# almost always filler in marketing copy, so flagging them risks no real false positives. The model's
# eye misses these under creative pressure; this catches what the polish pass overlooks.
BANNED_PHRASES = [
    "leverage the power", "fast-paced world", "unlock your potential", "to the next level",
    "supercharge", "game-changer", "game changer", "synergy", "harness", "delve", "tapestry",
    "navigate the landscape", "efficiently and sustainably", "drive engagement",
    "resonate with your audience", "talk of the town", "make it happen", "in today's world",
]


def lint_banned(text: str) -> list[str]:
    """Return the banned phrases literally present in the text (case-insensitive)."""
    low = (text or "").lower()
    return [p for p in BANNED_PHRASES if p in low]


def scrub_banned(text: str, rewrite_fn, max_passes: int = 2) -> str:
    """Deterministic guard: while any banned phrase survives, ask rewrite_fn to surgically replace it.
    rewrite_fn(hits: list[str], text: str) -> str. Bounded so a stubborn phrase can't loop forever."""
    for _ in range(max_passes):
        hits = lint_banned(text)
        if not hits:
            break
        text = rewrite_fn(hits, text)
    return text
